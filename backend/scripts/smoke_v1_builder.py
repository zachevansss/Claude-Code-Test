def load_builder_creds() -> BuilderApiKeyCreds:
    path = Path("/root/copytrade-builder-creds.json")
    if path.exists():
        data = json.loads(path.read_text())
        return BuilderApiKeyCreds(
            key=data["key"], secret=data["secret"], passphrase=data["passphrase"],
        )
    print(f"  (no {path} — using fallback creds from memory)")
    return _FALLBACK_BUILDER


def pick_active_token(client: ClobClient) -> tuple[str, str]:
    """Find a top-volume market with a live orderbook via Gamma API."""
    resp = httpx.get(
        "https://gamma-api.polymarket.com/markets",
        params={"active": "true", "closed": "false", "limit": 25,
                "order": "volume24hr", "ascending": "false"},
        timeout=10,
    )
    resp.raise_for_status()
    for m in resp.json():
        tids_raw = m.get("clobTokenIds")
        if not tids_raw:
            continue
        tids = json.loads(tids_raw) if isinstance(tids_raw, str) else tids_raw
        for tid in tids:
            if not tid or str(tid) == "0":
                continue
            try:
                book = client.get_order_book(str(tid))
            except Exception:
                continue
            if (getattr(book, "bids", None) or []) or (getattr(book, "asks", None) or []):
                return str(tid), (m.get("question") or "?")[:60]
    raise RuntimeError("no markets with live orderbooks in gamma top-25")


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--user-id", type=int, default=1)
    parser.add_argument("--price", type=float, default=0.01)
    parser.add_argument("--size", type=float, default=5.0)
    parser.add_argument("--confirm", action="store_true")
    args = parser.parse_args()

    db = SessionLocal()
    try:
        wallet = db.query(ManagedWallet).filter(ManagedWallet.user_id == args.user_id).first()
        if not wallet:
            print(f"no managed wallet for user_id={args.user_id}"); return 1
        eoa = wallet.address
        proxy = wallet.proxy_address
        priv = WalletManager.get_private_key_hex(wallet)
    finally:
        db.close()

    print(f"EOA:           {eoa}")
    print(f"proxy_address: {proxy}")
    if not proxy:
        print("ERROR: this flow only works for wallets with a proxy_address set.")
        return 2

    print("\nLoading builder credentials ...")
    builder_creds = load_builder_creds()
    print(f"  builder key: {builder_creds.key}")
    builder_config = BuilderConfig(local_builder_creds=builder_creds)

    print("\nInitializing ClobClient (V1, sig_type=1, funder=proxy, builder_config) ...")
    client = ClobClient(
        host=settings.polymarket_base_url,
        key=priv,
        chain_id=settings.polygon_chain_id,
        signature_type=1,             # SIG_POLY_PROXY
        funder=proxy,
        builder_config=builder_config,
    )
    creds = client.create_or_derive_api_creds()
    client.set_api_creds(creds)
    print(f"  user api_key:  {creds.api_key}")

    print("\nPicking an active market ...")
    token_id, market_desc = pick_active_token(client)
    print(f"  token_id:    {token_id}")
    print(f"  market:      {market_desc}")

    tick = client.get_tick_size(token_id)
    neg = client.get_neg_risk(token_id)
    print(f"  tick_size:   {tick}  neg_risk: {neg}")

    cost = args.price * args.size
    print(f"\nPlan: BUY {args.size} shares @ ${args.price:.3f} (${cost:.4f})")
    print(f"      deep out-of-bid; should be accepted but never fill.")
    if not args.confirm:
        print("\nDRY RUN — pass --confirm to broadcast.")
        return 0

    order_args = OrderArgs(
        token_id=token_id,
        side="BUY",
        price=args.price,
        size=args.size,
        fee_rate_bps=0,
    )

    print("\nSigning + posting via py-clob-client (will auto-attach builder headers) ...")
    try:
        resp = client.create_and_post_order(order_args)
    except Exception as e:  # noqa: BLE001
        print(f"\nFAILED: {type(e).__name__}: {e}")
        return 3
    print(f"Raw response: {resp}")

    order_id = (
        resp.get("orderID") or resp.get("order_id") or resp.get("orderHash")
        if isinstance(resp, dict) else None
    )
    success = isinstance(resp, dict) and resp.get("success", True)
    err = resp.get("errorMsg") if isinstance(resp, dict) else None

    if order_id and success and not err:
        print(f"\n--- RESULT: ACCEPTED ---")
        print(f"order_id: {order_id}")
        print(f"V1+Builder path works. Cancelling now ...")
        try:
            cancel_resp = client.cancel(order_id)
            print(f"cancel response: {cancel_resp}")
        except Exception as e:  # noqa: BLE001
            print(f"cancel failed (order still live, may auto-expire): {e}")
        return 0

    print(f"\n--- RESULT: REJECTED ---")
    print(f"errorMsg: {err}")
    return 4


if __name__ == "__main__":
    sys.exit(main())
