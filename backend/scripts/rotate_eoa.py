"""Rotate user_id=1's managed EOA to a brand-new wallet.

WHY: Polymarket has tagged the existing EOA server-side as a deposit-wallet
user and refuses pure-EOA orders ("maker address not allowed"). Rolling the
CLOB API key returned the SAME UUID (it's derived from the EOA signature),
which proves the block is at the address level, not the key level. A new
EOA that has never authenticated to Polymarket will register cleanly as a
self-funded user.

WHAT this script does:
  1. Reads the current managed_wallet for the user (old EOA + PK).
  2. Generates a fresh EOA via eth_account.Account.create() (CSPRNG).
  3. Writes the new EOA's private key to a backup file IMMEDIATELY, so the
     PK is never lost even if a later step fails. Also prints it.
  4. With --confirm:
       a. Sends a MATIC gas seed (default 0.5) from old -> new.
       b. Sends the full pUSD balance from old -> new.
       c. Runs src.wallet.approvals.setup_wallet on the new EOA (4 V2 txs).
       d. Replaces the managed_wallets row for the user (encrypted new PK,
          new address, proxy_address=NULL).

Run from backend/ on the VPS:
    python -m scripts.rotate_eoa --user-id 1                 # dry-run
    python -m scripts.rotate_eoa --user-id 1 --confirm        # execute

PRECONDITION: stop the bot service first (systemctl stop copytrade). The
script aborts if it detects the service is active, unless --skip-service-check
is passed.

RECOVERY: if any step fails after the new EOA is generated, the private key
is in the backup file printed at the start. Funds (if already moved) are at
the new address. To finish manually, use POST /wallet/import or call
WalletManager.import_for_user(replace_existing=True) with that PK.
"""
from __future__ import annotations

import argparse
import json
import os
import subprocess
import sys
import time
from datetime import datetime, timezone
from pathlib import Path

from eth_account import Account
from web3 import Web3

sys.path.insert(0, ".")

from src.config.settings import settings
from src.database.session import SessionLocal
from src.models import ManagedWallet
from src.wallet.approvals import setup_wallet
from src.wallet.balances import PUSD_ADDRESS, PUSD_DECIMALS
from src.wallet.manager import WalletManager

ERC20_ABI = [
    {"constant": True, "inputs": [{"name": "o", "type": "address"}], "name": "balanceOf",
     "outputs": [{"name": "", "type": "uint256"}], "type": "function"},
    {"constant": False, "inputs": [{"name": "to", "type": "address"},
                                    {"name": "value", "type": "uint256"}],
     "name": "transfer", "outputs": [{"name": "", "type": "bool"}], "type": "function"},
]

MATIC_RESERVE_WEI = 10**16  # 0.01 MATIC left in old EOA for dust


def send(w3: Web3, signer, tx_built) -> bytes:
    signed = signer.sign_transaction(tx_built)
    raw = getattr(signed, "raw_transaction", None) or getattr(signed, "rawTransaction")
    return w3.eth.send_raw_transaction(raw)


def wait(w3: Web3, h: bytes, label: str):
    print(f"  {label} tx: {h.hex()}")
    rcpt = w3.eth.wait_for_transaction_receipt(h, timeout=180)
    print(f"  {label} status: {rcpt.status} block={rcpt.blockNumber} gas={rcpt.gasUsed}")
    if rcpt.status != 1:
        raise SystemExit(f"  {label} reverted on-chain — aborting")
    return rcpt


def bot_service_active() -> bool | None:
    """Returns True/False if systemctl is available and reports a status,
    None if systemctl isn't available (e.g., running locally on Windows)."""
    if os.name == "nt":
        return None
    try:
        r = subprocess.run(
            ["systemctl", "is-active", "copytrade"],
            capture_output=True, text=True, timeout=5,
        )
        return r.stdout.strip() == "active"
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return None


def backup_new_eoa(address: str, pk_hex: str) -> Path:
    """Write the new EOA's PK to a stable backup file. Returns the path.
    Permissions: 0600 on POSIX."""
    backup_dir = Path.home() / "copytrade-eoa-backups"
    backup_dir.mkdir(parents=True, exist_ok=True)
    ts = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")
    path = backup_dir / f"rotate_eoa_{ts}_{address.lower()}.json"
    payload = {
        "address": address,
        "private_key": pk_hex,
        "created_utc": ts,
        "note": (
            "Generated by rotate_eoa.py. KEEP SECRET. After successful "
            "rotation this PK is also encrypted in managed_wallets in the "
            "DB, but this file is the only plaintext backup."
        ),
    }
    path.write_text(json.dumps(payload, indent=2))
    if os.name != "nt":
        try:
            path.chmod(0o600)
        except OSError:
            pass
    return path


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--user-id", type=int, default=1)
    parser.add_argument("--confirm", action="store_true",
                        help="actually broadcast txs and swap DB row")
    parser.add_argument("--matic-seed", type=float, default=0.5,
                        help="MATIC to send from old to new EOA (gas seed)")
    parser.add_argument("--skip-service-check", action="store_true",
                        help="don't refuse to run while copytrade.service is active")
    args = parser.parse_args()

    # --- 1. Read current managed wallet ---
    db = SessionLocal()
    try:
        wallet = (
            db.query(ManagedWallet)
            .filter(ManagedWallet.user_id == args.user_id)
            .first()
        )
        if not wallet:
            print(f"no managed wallet for user_id={args.user_id}"); return 1
        old_signer = WalletManager.get_signer(wallet)
        old_eoa = old_signer.address
        old_proxy = wallet.proxy_address
    finally:
        db.close()

    print(f"current EOA:    {old_eoa}")
    print(f"proxy_address:  {old_proxy or '(null — pure EOA mode)'}")

    if old_proxy:
        print("\nERROR: proxy_address is set on this wallet. This script is for the")
        print("pure-EOA rotation case. Clear proxy_address first (or use a different")
        print("rotation path).")
        return 2

    # --- 2. Service check ---
    svc = bot_service_active()
    if svc is True and not args.skip_service_check:
        print("\nERROR: copytrade.service is active. Stop it first:")
        print("    systemctl stop copytrade")
        print("(or pass --skip-service-check if you really want to proceed)")
        return 3
    if svc is None:
        print("(systemctl not available — skipping service check)")

    # --- 3. Read on-chain balances ---
    w3 = Web3(Web3.HTTPProvider(settings.polygon_rpc_url, request_kwargs={"timeout": 15}))
    chain_id = settings.polygon_chain_id
    pusd = w3.eth.contract(address=PUSD_ADDRESS, abi=ERC20_ABI)

    matic_wei = w3.eth.get_balance(old_eoa)
    pusd_raw = pusd.functions.balanceOf(old_eoa).call()
    matic = matic_wei / 1e18
    pusd_bal = pusd_raw / 10**PUSD_DECIMALS

    print(f"\nold EOA balances:")
    print(f"  MATIC: {matic:,.6f}")
    print(f"  pUSD:  {pusd_bal:,.6f}")

    if pusd_raw == 0 and matic_wei < int(args.matic_seed * 1e18):
        print("\nERROR: old EOA has nothing meaningful to transfer (no pUSD, MATIC too low).")
        return 4

    seed_wei = int(args.matic_seed * 1e18)
    if matic_wei < seed_wei + MATIC_RESERVE_WEI + 5 * 10**15:  # ~0.005 MATIC for gas
        print(f"\nERROR: old EOA MATIC too low to seed new EOA "
              f"(have {matic:.4f}, need at least {(seed_wei + MATIC_RESERVE_WEI + 5*10**15)/1e18:.4f}).")
        return 5

    # --- 4. Generate new EOA + back up PK ---
    new_account = Account.create()
    new_eoa = new_account.address
    new_pk_hex = "0x" + new_account.key.hex()
    backup_path = backup_new_eoa(new_eoa, new_pk_hex)

    print(f"\n--- NEW EOA generated ---")
    print(f"  address:        {new_eoa}")
    print(f"  backup file:    {backup_path}")
    print(f"  (the backup is plaintext PK — chmod 600 on POSIX. KEEP SECRET.)")

    # --- 5. Plan ---
    print(f"\n--- Plan ({'EXECUTE' if args.confirm else 'DRY RUN'}) ---")
    print(f"  1) send {args.matic_seed} MATIC: {old_eoa} -> {new_eoa}")
    print(f"  2) send {pusd_bal:,.6f} pUSD: {old_eoa} -> {new_eoa}")
    print(f"  3) run V2 approvals on new EOA (up to 4 txs)")
    print(f"  4) replace managed_wallets row for user_id={args.user_id} "
          f"(address -> {new_eoa}, proxy_address -> NULL)")

    if not args.confirm:
        print("\nDRY RUN — pass --confirm to broadcast.")
        print(f"New EOA PK saved to: {backup_path}")
        print("(Delete that file if you abandon this rotation — it contains a live private key.)")
        return 0

    # --- 6. Execute ---
    nonce = w3.eth.get_transaction_count(old_eoa)
    gp = w3.eth.gas_price
    print(f"\nGas price: {gp/1e9:.2f} gwei | nonce start: {nonce}")

    # Step 1: MATIC seed
    print(f"\n[1] Sending {args.matic_seed} MATIC -> new EOA ...")
    tx = {
        "from": old_eoa,
        "to": new_eoa,
        "value": seed_wei,
        "nonce": nonce,
        "gas": 21_000,
        "gasPrice": gp,
        "chainId": chain_id,
    }
    wait(w3, send(w3, old_signer, tx), "MATIC seed")
    nonce += 1
    time.sleep(2)

    # Step 2: pUSD transfer
    if pusd_raw > 0:
        print(f"\n[2] Sending {pusd_bal:,.6f} pUSD -> new EOA ...")
        tx = pusd.functions.transfer(new_eoa, pusd_raw).build_transaction({
            "from": old_eoa, "nonce": nonce, "gas": 120_000,
            "gasPrice": gp, "chainId": chain_id,
        })
        wait(w3, send(w3, old_signer, tx), "pUSD transfer")
        nonce += 1
        time.sleep(2)
    else:
        print("\n[2] Skipping pUSD transfer (balance is zero).")

    # Verify funds landed at new EOA
    new_matic = w3.eth.get_balance(new_eoa) / 1e18
    new_pusd = pusd.functions.balanceOf(new_eoa).call() / 10**PUSD_DECIMALS
    print(f"\nnew EOA balances after transfers:")
    print(f"  MATIC: {new_matic:,.6f}")
    print(f"  pUSD:  {new_pusd:,.6f}")

    # Step 3: approvals on new EOA
    print(f"\n[3] Running V2 approvals on new EOA ...")
    matic_for_approvals, actions = setup_wallet(new_account)
    for a in actions:
        print(f"    {a['contract']:30s} {a['status']:8s} {a['tx'] or ''}")

    # Step 4: swap DB row
    print(f"\n[4] Replacing managed_wallets row for user_id={args.user_id} ...")
    db = SessionLocal()
    try:
        new_wallet = WalletManager.import_for_user(
            user_id=args.user_id,
            private_key_hex=new_pk_hex,
            db=db,
            replace_existing=True,
            proxy_address=None,
        )
        db.commit()
        print(f"    managed_wallets.address = {new_wallet.address}")
        print(f"    managed_wallets.proxy_address = {new_wallet.proxy_address}")
    finally:
        db.close()

    # --- 7. Summary ---
    print(f"\n--- DONE ---")
    print(f"  old EOA: {old_eoa}  (residual MATIC stays here; transfer separately if desired)")
    print(f"  new EOA: {new_eoa}")
    print(f"  backup PK file: {backup_path}")
    print(f"\nNext:")
    print(f"  1) Restart the bot:        systemctl start copytrade")
    print(f"  2) Watch logs for first tick: journalctl -u copytrade -f")
    print(f"  3) Confirm /wallet endpoint shows the new address and balances.")
    print(f"  4) Update QUICK_REFERENCE.md to point at the new EOA.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
